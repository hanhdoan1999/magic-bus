package dto;
public class BusDto {
	private String idBus;
	private int soHang;
	private int soGheMoiHang;
	private String kiHieuDiaPhuong;
	private String bienSoDangKi;
	private int soThuTuDangKi;
	

	public String getIdBus() {
		return idBus;
	}
	public void setIdBus(String idBus) {
		this.idBus = idBus;
	}
	public int getSoHang() {
		return soHang;
	}
	public void setSoHang(int soHang) {
		this.soHang = soHang;
	}
	public int getSoGheMoiHang() {
		return soGheMoiHang;
	}
	public void setSoGheMoiHang(int soGheMoiHang) {
		this.soGheMoiHang = soGheMoiHang;
	}
	public String getKiHieuDiaPhuong() {
		return kiHieuDiaPhuong;
	}
	public void setKiHieuDiaPhuong(String kiHieuDiaPhuong) {
		this.kiHieuDiaPhuong = kiHieuDiaPhuong;
	}
	public String getBienSoDangKi() {
		return bienSoDangKi;
	}
	public void setBienSoDangKi(String bienSoDangKi) {
		this.bienSoDangKi = bienSoDangKi;
	}
	public int getSoThuTuDangKi() {
		return soThuTuDangKi;
	}
	public void setSoThuTuDangKi(int soThuTuDangKi) {
		this.soThuTuDangKi = soThuTuDangKi;
	}

}



